#Examples of Type Casting

#1 Summ
n1 = "10"
n2 = "77"
print(int(n1) + int(n2))

#2 String Concatenation
n3 = "This is summ of "
n4 = str(1.15)
n5 = str(2.22)
print(n3 + n4 + ' ' + n5)

#3 Integer Casting
a = 9.999999
print(int(a))

#4 Float Casting
b = 10
print(float(b))

#5 Complex Type Casting
c = 19.777777
c = float(str(int(c)))
print(c)